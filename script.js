// Fun√ß√£o para buscar os reposit√≥rios mais bem avaliados no GitHub
async function fetchTopRepositories() {
    const url = 'https://api.github.com/search/repositories?q=stars:>10000&sort=stars&order=desc&per_page=10';
    
    try {
        const response = await fetch(url);
        if (!response.ok) {
            throw new Error(`Erro na requisi√ß√£o: ${response.status}`);
        }
        
        const data = await response.json();
        displayRepositories(data.items);
    } catch (error) {
        console.error('Erro ao buscar reposit√≥rios:', error);
    }
}

// Fun√ß√£o para exibir os reposit√≥rios na p√°gina
function displayRepositories(repositories) {
    const container = document.getElementById('repo-list');
    container.innerHTML = '';
    
    repositories.forEach(repo => {
        const repoElement = document.createElement('div');
        repoElement.classList.add('repo');
        repoElement.innerHTML = `
            <h2><a href="${repo.html_url}" target="_blank">${repo.name}</a></h2>
            <p>${repo.description || 'Sem descri√ß√£o'}</p>
            <p><strong>‚≠ê ${repo.stargazers_count} | üñ•Ô∏è ${repo.language || 'Desconhecido'}</strong></p>
        `;
        container.appendChild(repoElement);
    });
}

// Chamar a fun√ß√£o ao carregar a p√°gina
document.addEventListener('DOMContentLoaded', fetchTopRepositories);